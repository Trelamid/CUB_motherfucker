#include "libft.h"
#include <stdlib.h>

void	*ft_calloc(size_t num, size_t size)
{
	void	*p;

	p = malloc(num * size);
	if (p == NULL)
		return (p);
	p = ft_memset(p, 0, num * size);
	return (p);
}
